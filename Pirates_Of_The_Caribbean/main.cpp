int main() {
    // ===== PARSE =====
    stringstream ss(songData);
    vector<Note> song;
    Note n;

    while (ss >> n.start >> n.note >> n.duration) {
        song.push_back(n);
    }

    cout << "Loaded notes: " << song.size() << endl;
    if (song.empty()) return 0;

    // ===== CONFIG =====
    const int sampleRate = 44100;

    int totalMs = 0;
    for (auto &n : song)
        totalMs = max(totalMs, n.start + n.duration);

    int totalSamples = (long long)sampleRate * totalMs / 1000;

    cout << "Total samples: " << totalSamples << endl;

    // ===== GENERATE AUDIO =====
    short* pcm = new short[totalSamples](); // zero-init

    for (auto &n : song) {
        int startSample = (long long)n.start * sampleRate / 1000;
        int endSample   = (long long)(n.start + n.duration) * sampleRate / 1000;

        double freq = midiToFreq(n.note);

        for (int i = startSample; i < endSample && i < totalSamples; i++) {
            double t = (double)i / sampleRate;
            double s = sin(2.0 * 3.1415926535 * freq * t) * 0.1;

            int value = pcm[i] + (int)(s * 20000);

            if (value > 32767) value = 32767;
            if (value < -32768) value = -32768;

            pcm[i] = (short)value;
        }
    }

    // ===== BUILD WAV =====
    int dataSize = totalSamples * sizeof(short);
    int fileSize = 36 + dataSize;

    char* wav = new char[44 + dataSize];
    char* p = wav;

    memcpy(p, "RIFF", 4); p += 4;
    memcpy(p, &fileSize, 4); p += 4;
    memcpy(p, "WAVE", 4); p += 4;

    memcpy(p, "fmt ", 4); p += 4;
    int fmtSize = 16;
    short format = 1;
    short channels = 1;
    int byteRate = sampleRate * 2;
    short blockAlign = 2;
    short bits = 16;

    memcpy(p, &fmtSize, 4); p += 4;
    memcpy(p, &format, 2); p += 2;
    memcpy(p, &channels, 2); p += 2;
    memcpy(p, &sampleRate, 4); p += 4;
    memcpy(p, &byteRate, 4); p += 4;
    memcpy(p, &blockAlign, 2); p += 2;
    memcpy(p, &bits, 2); p += 2;

    memcpy(p, "data", 4); p += 4;
    memcpy(p, &dataSize, 4); p += 4;

    memcpy(p, pcm, dataSize);

    // ===== DEBUG =====
    cout << "Playing sound..." << endl;

    BOOL ok = PlaySoundA(wav, NULL, SND_MEMORY | SND_SYNC);

    cout << "PlaySound returned: " << ok << endl;

    // ===== CLEANUP =====
    delete[] pcm;
    delete[] wav;

    return 0;
}
